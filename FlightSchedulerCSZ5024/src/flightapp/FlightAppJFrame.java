    private void AddNameSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddNameSubmitActionPerformed
        // TODO add your handling code here:
        if (evt.getSource() == AddNameSubmit) {
            
            final String name = AddNameTextField.getText();
            boolean isDupe = Customer.addCustomer(name);
            
            //checks for duplicate names in the database
            if (isDupe) {
                ArrayList<String> customers = Customer.getCustomer();
                
                BookNameJCombo.removeAllItems();
                CancelNameJCombo.removeAllItems();
                StatusJCombo.removeAllItems();
                for (String i : customers) {
                    BookNameJCombo.addItem(i);
                    CancelNameJCombo.addItem(i);
                    StatusJCombo.addItem(i);
                }
                AddNamePrompt.setText(name + " added successfully!");
                
            }
            else {
                AddNamePrompt.setText(name + " is already in use! please choose another");
            }
            
            AddNamePrompt.setVisible(true);
            
        }
        
    }//GEN-LAST:event_AddNameSubmitActionPerformed

    private void BookSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BookSubmitActionPerformed
        // TODO add your handling code here:
        if (evt.getSource() == BookSubmit) {
            
            //retrieve selections from JCombo boxes
            String selectedBDate = BookDateJCombo.getSelectedItem().toString();
            BookDateOutput.setText(selectedBDate);
            String selectedBFlight = BookFlightNumJCombo.getSelectedItem().toString();
            BookFlightNumOutput.setText(selectedBFlight);
            String selectedBName = BookNameJCombo.getSelectedItem().toString();
            BookCustomerOutput.setText(selectedBName);
            Timestamp timestamp = new Timestamp(Calendar.getInstance().getTime().getTime());
            
            //set JCombo selections in BookingEntry class
            BookingEntry book = new BookingEntry(selectedBName, selectedBFlight, selectedBDate, timestamp);
            boolean isFull = BookingQueries.flightFull(selectedBFlight, selectedBDate);
            
            if (isFull) {
                BookPrompt.setText("Flight full, added to Waitlist");
            }
            else {
                BookPrompt.setText("Flight Booked!");
            }
            
            boolean bookingsAdd = BookingQueries.addBooking(book, isFull);
            if (!bookingsAdd){
                BookPrompt.setText("Flight already Booked!");
            }
            BookPrompt.setVisible(true);
            BookDateOutput.setVisible(true);
            BookFlightNumOutput.setVisible(true);
            BookCustomerOutput.setVisible(true);
        }
    }//GEN-LAST:event_BookSubmitActionPerformed

