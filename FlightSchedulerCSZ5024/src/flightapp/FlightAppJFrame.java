    private void AddDateSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddDateSubmitActionPerformed
        // TODO add your handling code here:
        if (evt.getSource() == AddDateSubmit) {
            
            SimpleDateFormat formater = new SimpleDateFormat("yyyy-MM-dd");
            String spinnerValue = formater.format(AddDateSpinner.getValue());
            Date day = Date.valueOf(spinnerValue);
            
            //for some reason it wont let me compile Day.addDate(day) without adding a redundant try catch block
            try {
                if (Day.addDate(day) == true) {
                    AddDatePrompt.setText(day+" added successfully!");
                    
                    BookDateJCombo.removeAllItems();
                    CancelDateJCombo.removeAllItems();
                    StatusDateJCombo.removeAllItems();
                    WaitlistDateJCombo.removeAllItems();
                    for(String d: Day.getDay()) {
                        BookDateJCombo.addItem(d);
                        CancelDateJCombo.addItem(d);
                        StatusDateJCombo.addItem(d);
                        WaitlistDateJCombo.addItem(d);            
                    }  
                }
                else {
                    AddDatePrompt.setText(day+" already added, please select another date");
                }
                AddDatePrompt.setVisible(true);
                    
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_AddDateSubmitActionPerformed

